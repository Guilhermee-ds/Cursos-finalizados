<?php

/**
 *  Aula sobre Operadores de Atribuição
 * 
 * Nessa aula eu explico como ultilizar operadores aritméticos
 * e melhorar a codificaçã de expressões matemáticas com PHP.
 * 
 * Operadores : += -= /= *= %=
 */


$itemA = 50;

$itemA += 25;

echo $itemA . PHP_EOL;

$itemA-= 25;

echo $itemA . PHP_EOL;


$itemA /= 2;

echo $itemA . PHP_EOL;


$itemA *= 3;

echo $itemA . PHP_EOL;


$itemA %= 2;

echo $itemA . PHP_EOL;


