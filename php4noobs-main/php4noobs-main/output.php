<?php


/**
 * Aula sobre funções de saída
 * 
 * Nessa aula eu explico e ultilizo funções para você conseguir debugar
 * e visualizar o que está acontecendo no seu código
 * 
 * 
 * As funções a serem ultilizadas : echo, var_dump
 * Constantes a serem ultilizadas : PHP_EOL
 */


 echo "Hello, word" . "-" . 123 .PHP_EOL;

 var_dump(value: "Hi Dev");
 var_dump(value: true);
 var_dump(value: 1234);
 var_dump(value:[1,2,3,4,5]);   // no var_dump você não precisa usar o PHP_EOL, ele quebra a linha automatico.