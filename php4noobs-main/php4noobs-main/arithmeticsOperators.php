 echo $itemC . PHP_EOL;


// Divisão
 $itemA = 25;
 $itemB = 5;
 $itemC = ($itemA / $itemB);

 echo $itemC . PHP_EOL;


 // Multiplicação

 $itemA = 25;
 $itemB = 5;
 $itemC = ($itemA * $itemB);

 echo $itemC . PHP_EOL;


 // Resto

 $itemA = 20;
 $itemB = 2;
 $itemC = ($itemA % $itemB);

